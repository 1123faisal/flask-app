@app.route("/contact", methods=["GET", "POST"])
def contact():
    form = ContactForm()
    if form.validate_on_submit():
        mail_address = app.config['MAIL_ADDRESS']
        mail_password = app.config['MAIL_PASSWORD']
        if mail_address and mail_password:
            try:
                with smtplib.SMTP('smtp.gmail.com', 587) as connection:
                    connection.starttls()
                    connection.login(user=mail_address, password=mail_password)
                    connection.sendmail(
                        from_addr=mail_address,
                        to_addrs=mail_address,
                        msg=f"Subject: Blog Contact Form\n\n"
                            f"Name: {form.name.data}\n"
                            f"Email: {form.email.data}\n"
                            f"Phone: {form.phone.data}\n\n"
                            f"{form.message.data}"
                    )
                flash("Your message has been sent successfully!")
            except Exception:
                flash("Sorry, there was an error sending your message. Please try again later.")
        else:
            flash("Your message has been sent successfully!")
        return redirect(url_for('contact'))
    return render_template("contact.html", form=form)


@app.route("/new-post", methods=["GET", "POST"])
@login_required
def add_new_post():
    if current_user.id != 1:
        return redirect(url_for('get_all_posts'))
    form = CreatePostForm()
    if form.validate_on_submit():
        new_post = BlogPost(
            title=form.title.data,
            subtitle=form.subtitle.data,
            body=form.body.data,
            img_url=form.img_url.data,
            author=current_user,
            date=date.today().strftime("%B %d, %Y")
        )
        db.session.add(new_post)
        db.session.commit()
        return redirect(url_for("get_all_posts"))
    return render_template("make-post.html", form=form, is_edit=False)


@app.route("/edit-post/<int:post_id>", methods=["GET", "POST"])
@login_required
def edit_post(post_id):
    if current_user.id != 1:
        return redirect(url_for('get_all_posts'))
    post = BlogPost.query.get(post_id)
    edit_form = CreatePostForm(
        title=post.title,
        subtitle=post.subtitle,
        img_url=post.img_url,
        body=post.body
    )
    if edit_form.validate_on_submit():
        post.title = edit_form.title.data
        post.subtitle = edit_form.subtitle.data
        post.img_url = edit_form.img_url.data
        post.body = edit_form.body.data
        db.session.commit()
        return redirect(url_for("show_post", post_id=post.id))
    return render_template("make-post.html", form=edit_form, is_edit=True)


